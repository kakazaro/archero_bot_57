function loadConfig()
    local config = loadfile(configFile)
    if config ~= nil then
        config = config()
    end
    if config == nil then
        config = {}
    end
    local package = gg.getTargetPackage()
    if package == nil then
        package = 'none'
    end

    return config[package] or {}
end

function saveConfig(config)
    local package = gg.getTargetPackage()
    if package == nil then
        package = 'none'
    end
	gg.saveVariable({[package]=config}, configFile)
end

